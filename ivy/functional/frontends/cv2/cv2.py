@to_ivy_arrays_and_back
def split(m, mv=None):
    return tuple(ivy.squeeze(ivy.split(m, axis=-1)))


@to_ivy_arrays_and_back
def cvtColor(src, code: int, dst=None, dstCn: int = 0):
    # float16 dtype conversion is required when integer-type source
    # images are multiplied by floating-point factors, causing potential
    # numeric overflow. This occurs within conversion formulas, leading to
    # value wrapping around the defined range. For instance, in 8-bit integers,
    # results exceeding 255 loop back to 0 and continue beyond
    # (e.g., 256 -> 0; 257 -> 1; -1 -> 255; -2 -> 254). However, in color
    # conversion, the desired behavior is to clip values within 0 to 255,
    # irrespective of overflow (e.g., 256 -> 255; 257 -> 255; -1 -> 0; -2 -> 0).
    # The solution involves float16 conversion of the integer image,
    # followed by value clipping.
    if (
        code == COLOR_RGB2GRAY
        or code == COLOR_RGBA2GRAY
        or code == COLOR_BGR2GRAY
        or code == COLOR_BGRA2GRAY
    ):
        channels = split(src)

        if code == COLOR_RGB2GRAY or code == COLOR_RGBA2GRAY:
            r, g, b = channels[0], channels[1], channels[2]
        elif code == COLOR_BGR2GRAY or code == COLOR_BGRA2GRAY:
            b, g, r = channels[0], channels[1], channels[2]
        else:
            raise ivy.exceptions.IvyException("Invalid color conversion code")

        gray = 0.299 * r + 0.587 * g + 0.114 * b

        return ivy.asarray(gray).astype(src.dtype)
    elif code == COLOR_GRAY2RGB or code == COLOR_GRAY2BGR:
        return ivy.expand_dims(src, axis=-1).repeat(repeats=3, axis=-1)
    elif code == COLOR_GRAY2RGBA or code == COLOR_GRAY2BGRA:
        code = COLOR_GRAY2RGB if code == COLOR_GRAY2RGBA else COLOR_GRAY2BGR
        rgb_image = cvtColor(src, code)
        alpha_channel = ivy.full_like(rgb_image[..., :1], 255)
        return ivy.concat((rgb_image, alpha_channel), axis=-1)
    elif code == COLOR_RGB2XYZ or code == COLOR_BGR2XYZ:
        if code == COLOR_BGR2XYZ:
            src = src[:, :, [2, 1, 0]]
        rgb_to_xyz_conversion_matrix = ivy.array(
            [
                [0.412453, 0.357580, 0.180423],
                [0.212671, 0.715160, 0.072169],
                [0.019334, 0.119193, 0.950227],
            ]
        )

        return ivy.vecdot(src, rgb_to_xyz_conversion_matrix).astype(src.dtype)
    elif code == COLOR_XYZ2RGB or code == COLOR_XYZ2BGR:
        conversion_matrix = ivy.array(
            [
                [3.240479, -1.53715, -0.498535],
                [-0.969256, 1.875991, 0.041556],
                [0.055648, -0.204043, 1.057311],
            ]
        )
        res = (
            ivy.vecdot(src.astype(ivy.float16), conversion_matrix)
            .clip(0, 255)
            .astype(src.dtype)
        )
        if code == COLOR_XYZ2BGR:
            res = res[:, :, [2, 1, 0]]

        return res

    elif code == COLOR_RGB2YCrCb or code == COLOR_BGR2YCrCb:
        DELTA = 128
        channels = split(src)
        if code == COLOR_RGB2YCrCb:
            to_gray_code = COLOR_RGB2GRAY
            r, _, b = channels[0], channels[1], channels[2]
        elif code == COLOR_BGR2YCrCb:
            to_gray_code = COLOR_BGR2GRAY
            b, _, r = channels[0], channels[1], channels[2]
        else:
            raise ivy.exceptions.IvyException("Invalid color conversion code")

        Y = cvtColor(src, to_gray_code)

        Y = Y.astype(ivy.float16)
        r = r.astype(ivy.float16)
        b = b.astype(ivy.float16)

        Cr = ((r - Y) * 0.713 + DELTA).clip(0, 255)
        Cb = ((b - Y) * 0.564 + DELTA).clip(0, 255)

        res = ivy.zeros_like(src, dtype=src.dtype)
        res[:, :, 0] = Y
        res[:, :, 1] = Cr
        res[:, :, 2] = Cb
        return res
    elif code == COLOR_YCrCb2RGB or code == COLOR_YCrCb2BGR:
        # TODO: investigate small value differences in the blue channel (up to 3-4)
        DELTA = 128

        Y, Cr, Cb = split(src)

        Y = Y.astype(ivy.float16)
        Cr = Cr.astype(ivy.float16)
        Cb = Cb.astype(ivy.float16)

        r = Y + 1.403 * (Cr - DELTA)
        g = Y - 0.714 * (Cr - DELTA) - 0.344 * (Cb - DELTA)
        b = Y + 1.733 * (Cb - DELTA)

        r = r.clip(0, 255)
        g = g.clip(0, 255)
        b = b.clip(0, 255)

        res = ivy.zeros_like(src, dtype=src.dtype)

        if code == COLOR_YCrCb2RGB:
            res[:, :, 0] = r
            res[:, :, 1] = g
            res[:, :, 2] = b

        if code == COLOR_YCrCb2BGR:
            res[:, :, 0] = b
            res[:, :, 1] = g
            res[:, :, 2] = r

        return res

    else:
        raise ivy.exceptions.IvyNotImplementedException(
            "not implemented for this type of conversion yet"
        )
